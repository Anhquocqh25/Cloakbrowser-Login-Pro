def translate_tree(root: QObject) -> None:
    objects = [root, *root.findChildren(QObject)]
    for obj in objects:
        if isinstance(obj, QWidget) and obj.windowTitle():
            obj.setWindowTitle(tr(obj.windowTitle()))
        if isinstance(obj, QLabel):
            obj.setText(tr(obj.text()))
        elif isinstance(obj, QAbstractButton):
            obj.setText(tr(obj.text()))
            if obj.toolTip():
                obj.setToolTip(tr(obj.toolTip()))
        elif isinstance(obj, (QLineEdit, QPlainTextEdit)):
            obj.setPlaceholderText(tr(obj.placeholderText()))
        if isinstance(obj, QWidget) and obj.toolTip():
            obj.setToolTip(tr(obj.toolTip()))
        elif isinstance(obj, QGroupBox):
            obj.setTitle(tr(obj.title()))
        if isinstance(obj, QComboBox):
            for index in range(obj.count()):
                obj.setItemText(index, tr(obj.itemText(index)))
        if isinstance(obj, QTabWidget):
            for index in range(obj.count()):
                obj.setTabText(index, tr(obj.tabText(index)))
        if isinstance(obj, QTableWidget):
            for column in range(obj.columnCount()):
                item = obj.horizontalHeaderItem(column)
                if item:
                    item.setText(tr(item.text()))
            for row in range(obj.rowCount()):
                for column in range(obj.columnCount()):
                    item = obj.item(row, column)
                    if item:
                        item.setText(tr(item.text()))
        if isinstance(obj, QListWidget):
            for index in range(obj.count()):
                item = obj.item(index)
                item.setText(tr(item.text()))
        if isinstance(obj, QMenu):
            obj.setTitle(tr(obj.title()))
        if isinstance(obj, QAction):
            obj.setText(tr(obj.text()))


class TranslationEventFilter(QObject):
    def eventFilter(self, watched: QObject, event: QEvent) -> bool:
        if _language == "vi" and event.type() == QEvent.Show:
            QTimer.singleShot(0, lambda obj=watched: _translate_safely(obj))
        return False


def _translate_safely(obj: QObject) -> None:
    try:
        translate_tree(obj)
    except RuntimeError:
        pass


def install_i18n(app: QObject) -> TranslationEventFilter:
    event_filter = TranslationEventFilter(app)
    app.installEventFilter(event_filter)
    return event_filter
